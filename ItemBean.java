package Servion;

public class ItemBean
{
	int id;
	String name;
	int price;
	String view;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getView() {
		return view;
	}
	public void setView(String view) {
		this.view = view;
	}

	
}
